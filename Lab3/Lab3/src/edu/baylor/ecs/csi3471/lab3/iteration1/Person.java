package edu.baylor.ecs.csi3471.lab3.iteration1;

public class Person {

    private String name;
    private int idNumber;

    public Person() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getIdNumber() {
        return idNumber;
    }

    public void setIdNumber(int idNumber) {
        this.idNumber = idNumber;
    }
}


